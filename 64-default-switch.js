var myValue = 5;

switch (myValue) {

    case 0:
        console.log("myValue is 0");
        break;
    case 1:
        console.log("myValue is 1");
        break;
    case 2:
        console.log("myValue is 2");
        break;
    default:
        console.log("myValue is not 0, 1 or 2");
        break;
}